const initialStatePage = {
  isLoading: false,
  showPassword: false,
};

const AuthPageReducer = (state = initialStatePage, action) => {
  switch (action.type) {
    case "SET_LOADING":
      return {
        ...state,
        isLoading: true,
      };
    case "TOGGLE_SHOW_PASSWORD":
      return {
        ...state,
        showPassword: !state.showPassword,
      };
    default:
      return state;
  }
};

export default AuthPageReducer;
